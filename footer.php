<script>
$(document).ready(function() {
  $('.shopcart-icon').on('click', function() {
    $.ajax({
      url: 'get_cart.php',
      type: 'GET',
      dataType: 'json',
      success: function(data) {
        let total = 0;
        let html = '';
        let itemCount = 0;

        if (Array.isArray(data) && data.length > 0) {
          data.forEach(item => {
            itemCount += parseInt(item.qty);
            total += parseFloat(item.total);
            html += `
  <li class="product-cart mini_cart_item" data-id="${item.id}">
    <div class="product-details">
      <a href="#" class="product-media" style="padding-left:10px !important">
        <img style="width:100%;height:50px" src="Admin/Product_Image/${item.product_image}" alt="img" class="mCS_img_loaded">
      </a>
      <h5 class="product-name">${item.product_name}</h5>
      <span class="product-price">$${item.product_price}</span>
      <span class="product-quantity">(x${item.qty})</span>
      <div class="product-remove">
        <a href="javascript:void(0);" class="remove-item" data-id="${item.id}">
          <i class="fa fa-trash-o" aria-hidden="true"></i>
        </a>
      </div>
    </div>
  </li>
`;
          });

          $('#cartItems').html(html);
          $('#subtotal').text(`$${total.toFixed(2)}`);
          $('.count').text(itemCount); // Show number of unique items
          $('.shopcart-description').show();
          $('#emptyCart').hide();
        } else {
          // If cart is empty
          $('#cartItems').html('');
          $('#subtotal').text(`$0.00`);
          $('.count').text(0);
          $('.shopcart-description').hide();
          $('#emptyCart span').text('0 item(s)');
          $('#emptyCart').show();
        }
      },
      error: function(xhr, status, error) {
        console.error("AJAX Error:", error);
      }
    });
  });

  // Use event delegation to handle click events on dynamically generated .remove-item elements
  $(document).on('click', '.remove-item', function() {
    const id = $(this).data('id');
    $.ajax({
      url: 'delete_cart_item.php',
      type: 'POST',
      data: { id },
      success: function(response) {
        if (response.trim() === 'success') {
          $('.shopcart-icon').click(); // Refresh cart after removing item
        } else {
          alert('Failed to remove item.');
        }
      }
    });
  });
});
</script>


